export function useTemples() {
  const language = useBookingStore((state) => state.language);
  
  const localizedTemples = temples.map(temple => {
    const langData = templeTranslations[language]?.[temple.id];
    if (!langData) return temple;
    
    // Deep merge the translated fields
    return {
      ...temple,
      name: langData.name || temple.name,
      deity: {
        ...temple.deity,
        ...langData.deity
      },
      history: {
        ...temple.history,
        ...langData.history
      },
      visitingInfo: {
        ...temple.visitingInfo,
        ...langData.visitingInfo
      },
      vipDarshanInfo: {
        ...temple.vipDarshanInfo,
        ...langData.vipDarshanInfo
      }
    };
  });
  
  return localizedTemples;
}
